#define SSC_BOOT_OFF_GAME_CNT		0x00
#define SSC_BOOT_OFF_GAME1_TYPE 	0x01
#define SSC_BOOT_OFF_GAME2_TYPE 	0x02
#define SSC_BOOT_OFF_GAME3_TYPE 	0x03
#define SSC_BOOT_OFF_GAME4_TYPE 	0x04
#define SSC_BOOT_OFF_GAME1_NAME 	(0x80+0x20*0)
#define SSC_BOOT_OFF_GAME2_NAME 	(0x80+0x20*1)
#define SSC_BOOT_OFF_GAME3_NAME 	(0x80+0x20*2)
#define SSC_BOOT_OFF_GAME4_NAME 	(0x80+0x20*3)

#define SSC_BOOT_GAMETYPE_NONE	0x00
#define SSC_BOOT_GAMETYPE_256K	0x01 //linked
#define SSC_BOOT_GAMETYPE_128K	0x02
#define SSC_BOOT_GAMETYPE_64K	0x03

//-----------------------------------------------------------------------------

void ssc_boot_addentry( ssc_cookie *ssc, uint32 bankmask, uint8 *header )
{
	bool menusane;
	uint8 menu[512];
	uint8 headerchecksum;
	int i;
	
	dprintf( "Installing boot menu\n" );
	
	// don't install/update menu if it's a 512k rom.
	if( bankmask==0 || (bankmask&0x1) )
		return;

	ssc_reset( ssc );
	if( ssc_setmode(ssc,SSC_CMD_READ_BANK_0) != B_OK )
		return;
	if( ssc_read(ssc,menu,sizeof(menu)) != sizeof(menu) )
		return;
		
	// does this look sane?
	menusane = memcmp(menu+5,ssc_boot+5,0x80-5)==0 && memcmp(menu+256,ssc_boot+256,sizeof(menu)-256)==0;
	if( !menusane )
		memcpy( menu, ssc_boot, 256 );
		
	if( bankmask == 0x02 )
	{
		memcpy( menu+SSC_BOOT_OFF_GAME1_NAME, header+0x34, 16 );
		if( menu[SSC_BOOT_OFF_GAME1_TYPE] == SSC_BOOT_GAMETYPE_NONE ) menu[SSC_BOOT_OFF_GAME_CNT]++;
		menu[SSC_BOOT_OFF_GAME1_TYPE] = SSC_BOOT_GAMETYPE_64K;
	}
	else if( bankmask == 0x04 )
	{
		memcpy( menu+SSC_BOOT_OFF_GAME2_NAME, header+0x34, 16 );
		if( menu[SSC_BOOT_OFF_GAME2_TYPE] == SSC_BOOT_GAMETYPE_NONE ) menu[SSC_BOOT_OFF_GAME_CNT]++;
		menu[SSC_BOOT_OFF_GAME2_TYPE] = SSC_BOOT_GAMETYPE_128K;
	}
	else if( bankmask == 0x08 )
	{
		memcpy( menu+SSC_BOOT_OFF_GAME3_NAME, header+0x34, 16 );
		if( menu[SSC_BOOT_OFF_GAME3_TYPE] == SSC_BOOT_GAMETYPE_NONE ) menu[SSC_BOOT_OFF_GAME_CNT]++;
		else if( menu[SSC_BOOT_OFF_GAME3_TYPE] == SSC_BOOT_GAMETYPE_256K )
			menu[SSC_BOOT_OFF_GAME4_TYPE] = SSC_BOOT_GAMETYPE_NONE;
		menu[SSC_BOOT_OFF_GAME3_TYPE] = SSC_BOOT_GAMETYPE_128K;
	}
	else if( bankmask == 0x10 )
	{
		memcpy( menu+SSC_BOOT_OFF_GAME4_NAME, header+0x34, 16 );
		if( menu[SSC_BOOT_OFF_GAME4_TYPE] == SSC_BOOT_GAMETYPE_NONE ) menu[SSC_BOOT_OFF_GAME_CNT]++;
		else if( menu[SSC_BOOT_OFF_GAME4_TYPE] == SSC_BOOT_GAMETYPE_256K )
			menu[SSC_BOOT_OFF_GAME3_TYPE] = SSC_BOOT_GAMETYPE_NONE;
		menu[SSC_BOOT_OFF_GAME4_TYPE] = SSC_BOOT_GAMETYPE_128K;
	}
	else if( bankmask == 0x18 )
	{
		memcpy( menu+SSC_BOOT_OFF_GAME3_NAME, header+0x34, 16 );
		if( menu[SSC_BOOT_OFF_GAME3_TYPE]==SSC_BOOT_GAMETYPE_NONE && menu[SSC_BOOT_OFF_GAME4_TYPE]==SSC_BOOT_GAMETYPE_NONE ) menu[SSC_BOOT_OFF_GAME_CNT]++;
		else if( menu[SSC_BOOT_OFF_GAME3_TYPE]==SSC_BOOT_GAMETYPE_128K && menu[SSC_BOOT_OFF_GAME4_TYPE]==SSC_BOOT_GAMETYPE_128K ) menu[SSC_BOOT_OFF_GAME_CNT]--;
		menu[SSC_BOOT_OFF_GAME3_TYPE] = SSC_BOOT_GAMETYPE_256K;
		menu[SSC_BOOT_OFF_GAME4_TYPE] = SSC_BOOT_GAMETYPE_256K;
	}
	
	dprintf( "Game cnt=%d [%d] [%d] [%d] [%d]\n",
		menu[SSC_BOOT_OFF_GAME_CNT],
		menu[SSC_BOOT_OFF_GAME1_TYPE],
		menu[SSC_BOOT_OFF_GAME2_TYPE],
		menu[SSC_BOOT_OFF_GAME3_TYPE],
		menu[SSC_BOOT_OFF_GAME4_TYPE] );
	
	ssc_reset( ssc );
	if( ssc_clearflash(ssc,0x01) != B_OK )
		return;

	headerchecksum = 0;
	for( i=0x134; i<0x14d; i++ ) headerchecksum += ssc_boot[i];
	headerchecksum = 0xe7-headerchecksum;
	dprintf( "New checksum: %02x\n", headerchecksum );

	if( ssc_setmode(ssc,SSC_CMD_WRITE_BANK_0) != B_OK ) return;
	if( ssc_write(ssc,menu,256) != 256 ) return;
//	if( ssc_write(ssc,ssc_boot+(256+0x00),0x4d) != 0x4d ) return;
//	if( ssc_write(ssc,&headerchecksum,1) != 1 ) return;
//	if( ssc_write(ssc,ssc_boot+(256+0x4e),sizeof(ssc_boot)-(256+0x4e)) != sizeof(ssc_boot)-(256+0x4e) ) return;
	if( ssc_write(ssc,ssc_boot+256,sizeof(ssc_boot)-256) != sizeof(ssc_boot)-256 ) return;
}

